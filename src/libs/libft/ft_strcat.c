#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *buf;

	buf = s1;
	while (*buf)
		buf++;
	while (*s2)
		*buf++ = *s2++;
	*buf = '\0';
	return (s1);
}
