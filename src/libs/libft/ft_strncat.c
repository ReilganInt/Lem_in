#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *buf;

	buf = s1;
	while (*buf)
		buf++;
	while (n-- && *s2)
		*buf++ = *s2++;
	*buf++ = '\0';
	return (s1);
}
